public class incriment {
    public static void main(String[] args) {
        int a=5;
        System.out.println("value of a="+a);
        System.out.println("value of in pre-increment="+(++a));
        System.out.println("value of post-incement"+(a++));
        System.out.println("value of a="+a);
        System.out.println("value of in pre-decrement="+(--a));
        System.out.println("value of post-decrement"+(a--));

    }
}
